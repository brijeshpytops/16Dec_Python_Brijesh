# C. Comparison operators
#     - Equality (==)
#     - Inequality (!=)
#     - Greater than (>)
#     - Less than (<)
#     - Greater than or equal to (>=)
#     - Less than or equal to (<=)

# Example:

num1 = 10

num2 = 20

result = num1 == num2

print(result)  # Output: False

result = num1 != num2

print(result)  # Output: True

result = num1 > num2

print(result)  # Output: False

result = num1 < num2

print(result)  # Output: True

result = num1 >= num2

print(result)  # Output: False

result = num1 <= num2

print(result)  # Output: False