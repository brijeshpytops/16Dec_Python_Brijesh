# E. Membership operators
#     - In (in)
#     - Not in (not in)

# Example

name = "python"

print("p" in name)  # Output: True

print("P" in name)  # Output: False

print("r" not in name)  # Output: True

print("o" not in name)  # Output: False

print('tho' in name) # True

print('hn' not in name) # True

